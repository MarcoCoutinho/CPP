#include <iostream>
using namespace std;
// programa para ilustrar o uso de operadores relacionais
int main ()
{
	int a, b;
	cout << "Digite um número --> a = ";
	cin >> a;
	cout << "Digite um número --> b = ";
	cin >> b;
	cout << "a < b --> " << (a<b) << endl;
	cout << "a > b --> " << (a>b) << endl;
	cout << "a == b --> " << (a==b) << endl;
	cout << " a != b --> " << (a!=b) << endl << endl;
	system("PAUSE");
	return 0;
}
