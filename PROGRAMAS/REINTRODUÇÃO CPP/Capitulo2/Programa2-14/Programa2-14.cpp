#include <iostream>
using namespace std;
//Programa para ilustrar o uso do operador módulo (%)

int main()
{
	cout << "Resto da divisão de 1 por 4: " << 1%4 << endl;
	cout << "Resto da divisão de 2 por 4: " << 2%4 << endl;
	cout << "Resto da divisão de 3 por 4: " << 3%4 << endl;
	cout << "Resto da divisão de 4 por 4: " << 4%4 << endl;
	cout << "Resto da divisão de 5 por 4: " << 5%4 << endl << endl;
	system ("PAUSE");
	return 0;
}
